    public void LlenarBotones(){
        JButton[] botones = new JButton[10];
        botones[0] = b1;
        botones[1] = b2;
        botones[2] = b3;
        botones[3] = b4;
        botones[4] = b5;
        botones[5] = b6;
        botones[6] = b7;
        botones[7] = b8;
        botones[8] = b9;
        botones[9] = b10;
        ArrayList<Integer> lista= new ArrayList();
        for(int i = 0; i < 10; i++){
            lista.add(i);
        }
        contenedor.setLayout (new GridLayout(3,4));
        for(int x = 0; x < botones.length; x++){
            int aleatorio = (int) Math.floor(Math.random()*lista.size());
            botones[x].setText(String.valueOf(lista.get(aleatorio)));
            lista.remove(aleatorio);  // 3 filas y 3 columnas)
            contenedor.add(botones[x]);
        }
        contenedor.add(bborrar);
        
        

        
    }
