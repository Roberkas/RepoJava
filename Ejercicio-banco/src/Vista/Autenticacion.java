    private void bentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bentrarActionPerformed
        if(ValidarDni()){
            if(ValidarClave()){
                Persona usuario = ejercicio.banco.EjercicioBanco.Login(tfnif.getText(), Integer.parseInt(tfclave.getText()));
                if(usuario!=null){
                    ejercicio.banco.EjercicioBanco.AbrirPanelControl(usuario);
                    ejercicio.banco.EjercicioBanco.Salir(this);
                }
                else{
                    JOptionPane.showMessageDialog( this, "El usuario o clave son incorrectos");
                }
            }
            else{
                    JOptionPane.showMessageDialog( this, "El clave no tiene la longitud apropiada");
            }
        }
        else{
            JOptionPane.showMessageDialog( this, "El dni no tiene el formato apropiado");
            }
    }//GEN-LAST:event_bentrarActionPerformed

