    public boolean ValidarProducto(){
        boolean ok = false;
        try{
            String producto = tfproducto.getText();
            for(String elemento : practica.ig4.PracticaIG4.productos){
                if(producto.equalsIgnoreCase(elemento)){
                    ok = true;
                }            
            }
            if(!ok){
                throw new Exception("Debes ingresar un producto de los ya definidos");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "ERROR -->" + e.getMessage());
        }
        return true;
        
    }
    
    public boolean ValidarUnidades(){
        try{
            if(!tfunidades.getText().isEmpty()){
                Integer.parseInt(tfunidades.getText());
            }
            else{
                throw new Exception("El campo \"Unidades\" no debe estar vacio");
            }
            
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Solo se pueden introducir digitos en el campo unidades");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "ERROR -->" + e.getMessage());
        }
        return true;
    }

